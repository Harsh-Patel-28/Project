function off(){
    document.getElementById("box").style.backgroundColor="white"
    document.getElementById("text").innerHTML="Turn OFF"
    document.getElementById("box").style.boxShadow = "0px 0px 0px black";

}
function on(){
    document.getElementById("box").style.backgroundColor="lightblue"
    document.getElementById("text").innerHTML="Turn ON"
    document.getElementById("box").style.boxShadow = "0px 0px 30px blue";

}